test("inserts inter-request delay between instance attempts", async () => {
  let requestCount = 0;
  const times: number[] = [];

  const fetchFn: FetchFn = async (url) => {
    if (url.includes("/api/v1/captions/") && !url.includes("label=")) {
      times.push(Date.now());
      requestCount++;
      return { ok: false, status: 500, text: async () => "", json: async () => ({}) };
    }
    return { ok: false, status: 404, text: async () => "", json: async () => ({}) };
  };

  const delayMs = 50;
  await fetchProxyTranscript("VIDEOID", fetchFn, {
    invidiousInstances: [INV_INSTANCE_1, INV_INSTANCE_2],
    pipedInstances: [],
    interRequestDelayMs: delayMs,
    cooldownMs: 0,
    requestTimeoutMs: 5_000,
  });

  expect(requestCount).toBe(2);
  // The gap between the two requests should be >= delayMs (with some tolerance)
  expect(times.length).toBe(2);
  expect((times[1] ?? 0) - (times[0] ?? 0)).toBeGreaterThanOrEqual(delayMs - 5);
}, 5_000);

