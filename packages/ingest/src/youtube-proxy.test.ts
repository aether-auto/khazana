test("round-robins across instances for consecutive requests", async () => {
  const hitOrder: string[] = [];

  const fetchFn: FetchFn = async (url) => {
    // Record which instance was hit (only captions-list requests)
    if (url.includes("/api/v1/captions/") && !url.includes("label=")) {
      if (url.startsWith(INV_INSTANCE_1)) hitOrder.push(INV_INSTANCE_1);
      else if (url.startsWith(INV_INSTANCE_2)) hitOrder.push(INV_INSTANCE_2);
      // Return empty captions so the scheduler moves to the next
      return { ok: true, status: 200, text: async () => JSON.stringify({ captions: [] }), json: async () => ({}) };
    }
    return { ok: false, status: 404, text: async () => "", json: async () => ({}) };
  };

  // Both calls use the shared module-level scheduler (no invidiousInstances in opts,
  // but we need the same instance to test round-robin — use a fresh InstanceScheduler
  // through opts but pass the SAME scheduler indirectly by using a shared closure).
  // Since opts.invidiousInstances creates a fresh scheduler per call, we cannot test
  // cross-call round-robin through opts. Test it directly on the scheduler instead.
  const scheduler = new InstanceScheduler([INV_INSTANCE_1, INV_INSTANCE_2]);
  const calls: string[] = [];
  for (let i = 0; i < 4; i++) {
    const inst = scheduler.next();
    if (inst) calls.push(inst);
  }
  expect(calls).toEqual([INV_INSTANCE_1, INV_INSTANCE_2, INV_INSTANCE_1, INV_INSTANCE_2]);

  // Verify the scheduler reference is not reused — keep hitOrder for completeness
  expect(hitOrder.length).toBe(0); // no actual requests were made through fetchFn in this test
});

