test("InstanceScheduler round-robins through instances", () => {
  const s = new InstanceScheduler(["https://a.com", "https://b.com", "https://c.com"]);
  const seen: string[] = [];
  for (let i = 0; i < 6; i++) {
    const inst = s.next();
    if (inst) seen.push(inst);
  }
  // Should cycle: a → b → c → a → b → c
  expect(seen).toEqual([
    "https://a.com",
    "https://b.com",
    "https://c.com",
    "https://a.com",
    "https://b.com",
    "https://c.com",
  ]);
});

test("InstanceScheduler skips cooled instances", () => {
  const s = new InstanceScheduler(["https://a.com", "https://b.com"]);
  const now = Date.now();
  // Cool down 'a' for 60s from now
  s.cooldown("https://a.com", 60_000, now);
  // next() should skip 'a' and return 'b'
  expect(s.next(now)).toBe("https://b.com");
});

test("InstanceScheduler returns null when all instances are cooled", () => {
  const s = new InstanceScheduler(["https://a.com", "https://b.com"]);
  const now = Date.now();
  s.cooldown("https://a.com", 60_000, now);
  s.cooldown("https://b.com", 60_000, now);
  expect(s.next(now)).toBeNull();
});

test("InstanceScheduler respects cooldown expiry", () => {
  const s = new InstanceScheduler(["https://a.com"]);
  const now = Date.now();
  s.cooldown("https://a.com", 1_000, now);
  // Before expiry
  expect(s.next(now + 500)).toBeNull();
  // After expiry
  expect(s.next(now + 1_001)).toBe("https://a.com");
});

test("InstanceScheduler reset clears cooldowns and restarts round-robin", () => {
  const s = new InstanceScheduler(["https://a.com", "https://b.com"]);
  const now = Date.now();
  s.cooldown("https://a.com", 60_000, now);
  s.next(now); // advances index to b
  s.reset();
  // After reset: no cooldowns, index back to 0
  expect(s.next(now)).toBe("https://a.com");
});

